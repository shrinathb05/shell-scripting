# Variables represent:

# server names

# IPs

# file paths

# counts

# flags (True/False)

# Examples:-

server_name = "web_server_01"
ip_address = "192.168.23.56"
file_path = "/var/www/html/index.html"
user_count = 150
is_active = True

# Explanation:-
# 1. server_name is a string variable representing the name of a server.
# 2. ip_address is a string variable representing the IP address of the server.
# 3. file_path is a string variable representing the path to a file on the server
# 4. user_count is an integer variable representing the number of users.
# 5. is_active is a boolean variable indicating whether the server is active or not.
# Variables are essential for storing and manipulating data in programming.
# They allow us to reference and use this data throughout our code.
# Variables can be of different data types, such as strings, integers, floats, and booleans.
# Understanding how to use variables effectively is crucial for writing efficient and logical code.
# Variables can be named according to specific rules:
# 1. They must start with a letter (a-z, A-Z) or an underscore (_).
# 2. They can contain letters, digits (0-9), and underscores.
# 3. They cannot be a reserved keyword in the programming language being used.
# 4. They are case-sensitive (e.g., myVar and myvar are different variables).
# By following these rules, we can create meaningful and descriptive variable names that enhance code readability and maintainability.
# In summary, variables are fundamental components of programming that allow us to store, manipulate, and reference data efficiently.
# Mastering the use of variables is essential for developing logical and effective code.    
