# Python Operators

# 01.Arithmetic operators
x = 2
y = 4

# Addition
total = x + y
print(total)

# Substraction
total = x - y
print(total)

# Multiplication
total = x * y
print(total)

# Division
total = y / x
print(total)

# Modulus
total = y % x
print(total)

# Power square
total = y ** x
print(total)

