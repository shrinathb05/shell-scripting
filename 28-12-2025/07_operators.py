# Comparision Operator
# This operator will print values in boolen format like True or False
a = 30
b = 60

out = a < b     # less than 
print(out)

out = a > b     # greater than 
print(out)

out = a != b    # not equal to
print(out)

out = a <= b    # less than equal
print(out)

out = a >= b    # greater than equal
print(out)

